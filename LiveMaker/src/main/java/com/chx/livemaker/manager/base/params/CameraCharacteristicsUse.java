public class CameraCharacteristicsUse {

    private int lensFacingType;

    private int focusMode;

    private int flashMode;

    private int sceneMode;

    private int effectMode;

    private int videoStabilizationMode;

    private int awbMode;

    public int getLensFacingType() {
        return lensFacingType;
    }

    public void setLensFacingType(int lensFacingType) {
        this.lensFacingType = lensFacingType;
    }

    public int getFocusMode() {
        return focusMode;
    }

    public void setFocusMode(int focusMode) {
        this.focusMode = focusMode;
    }

    public int getFlashMode() {
        return flashMode;
    }

    public void setFlashMode(int flashMode) {
        this.flashMode = flashMode;
    }

    public int getSceneMode() {
        return sceneMode;
    }

    public void setSceneMode(int sceneMode) {
        this.sceneMode = sceneMode;
    }

    public int getEffectMode() {
        return effectMode;
    }

    public void setEffectMode(int effectMode) {
        this.effectMode = effectMode;
    }

    public int getVideoStabilizationMode() {
        return videoStabilizationMode;
    }

    public void setVideoStabilizationMode(int videoStabilizationMode) {
        this.videoStabilizationMode = videoStabilizationMode;
    }

    public int getAwbMode() {
        return awbMode;
    }

    public void setAwbMode(int awbMode) {
        this.awbMode = awbMode;
    }
}
